                <h2>FAQs</h2>
                <h3>Where do you find your beads?</h3>
                    <p>The beads used in our designs come from a number of sources depending on the type. The focal, or featured beads are obtained from artisans around the world working in primarily in glass. Occasionally focal beads are made of other materials such as polymer clay.</p>
                <h3>Are these actual typewriter keys?</h3>
                      <p>Yes! All of our typewriter keys are authentic, from real typewriters. Most have been used in various environments and then stored in a garage, shed, barn or attic until salvaged and re-purposed into beautiful jewelry.</p>
                <h3>I have large wrists, can I have something custom sized?</h3>
                      <p>Yes! Our bracelets are designed to fit an average wrist, but if you need them smaller or larger we will do our best to accommodate your needs. Sizing is usually accomplished by adding or subtracting elements of the original design.</p>
                <h3>Do you ship worldwide?</h3>
                      <p>Yes! We have shipped Bead-n-Type designs all over the world.</p>
                <h3>Where do you sell your designs?</h3>
The primary outlet for Bead-n-Type designs is on-line at this site and other virtual shopfronts. We also set up a booth at various art and craft shows. Click “Where to Shop” on the main navigation bar to find links for on-line shops and events we will be attending.
               <h3>Do you offer wholesale pricing?</h3>
               <p>Not at this time. Our designs are primarily one-of-a-kind and are not well suited to the bulk ordering required of wholesale work. Discount pricing may be available on a case-by-case basis for group and larger orders.</p>
               <h3>How can I contact Bead-n-Type?</h3>
               <p>Click <a href="contacts/contact.php">“Contact Us”</a> on the main navigation bar or at the bottom of the page to find a contact form and links to email, social networks, and other ways of getting our attention!</p>
               <h3>Where do you draw your inspiration?</h3>
<p>Design inspiration often comes from the focal beads themselves; for typewriter key designs words are the focus.
When creating a beaded piece the color, shape, size and design of the focal bead is carefully considered. It is matched with supporting elements: smaller artisan beads, crystals, stones, findings and other materials. Choices and considerations of symmetry, asymmetry, compliment and contrast are made. The result is an often one-of-a-kind piece of art combining the artistry of the focal bead artisan and the designer.
Typewriter key jewelry begins with the building blocks of language, letters and symbols. Most of the pieces are intended to 'speak' or convey meaning and ideas. Bracelets are words, phrases or reminders of larger ideas. Pendants and earrings often play on words, using the letters and charms to pun or evoke a concept. Occasionally a piece will simply be just letters and the charm of wearing history.</p>
<h3>Do you make each item yourself or do you have a team that makes your designs?</h3>
<p>Each item is personally hand crafted from artisan-crafted hard and soft glass beads, authentic typewriter keys, and quality supporting materials.</p>
<h3>What does “Artisan lampwork glass” mean?</h3>
<p>Lampworking uses an open flame to melt rods or tubes of glass. Once molten, the glass is shaped using tools, forms and sometimes blowing to create beads, figures, marbles, vessels, ornaments, etc. Originally oil lamps were used to generate the flame, modern artists use a gas torch. In the 14th century the Murano region of Italy became a famous center for flameworked glass.</p>   
