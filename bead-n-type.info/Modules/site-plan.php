            </p>
        
            <p>Typography:  Main - Century Gothic, 14 pt, sans-serif, primarily black. 
			 Navigation Bar: Arial Unicode MS, 9pt, sans-serif, primarily white. 
            <P>Navigation:  The navigation bar/menu will be below main heading and logo. Horizontally centered. Drop-down menus.
