            <h2>
                Website Outline - Site Plan
            </h2>
            <p><b>Description</b></p>
            
            <p>Site Name:   Bead-n-Type</p>
            
            <p>Purpose:   On online portfolio showing the designs of Misty Darrington, past and present. Information regarding her products, inspiration and processes. Links to her Etsy shop, social network platforms, event calendar and contact information. </p>
            
            <p>Target Audience:   Bead-n-Type’s audience includes those who are looking for original, eclectic jewelry and accessories. Someone who appreciates quality, artisan, handmade and rare components combined in a one-of-a-kind item. 
Female customers will be those who are comfortable indulging on themselves or who are looking for something very special as a gift. 
Male customer will be shopping for someone special, generally directed to the website from this special person. 
            </p>
            
            <p>Persona:  <img src="/site-plan-images/persona-49KB.jpg" alt="personas" width="100%"</p>
            
            <p>Scenarios:   Where do you find your beads?
		Are these actual typewriter keys? 
		I have large wrists, can I have something custom sized?
		Do you ship worldwide?
		Where do you sell your designs? 
		Do you offer wholesale pricing?
		How can I contact Bead-n-Type? 
		Where do you draw your inspiration?
		Do you make each item yourself or do you have a team that makes your designs?
		What does “Artisan lampwork glass” mean? How is this different from other glass?
		How do I care for my jewelry?
		Can I have it gift boxed?
		Can I order for my bridal party?
            </p>
            
            <p><b>Asset List</b></p>
            
            <p>Common Theme: 	Each page will be contained in an identical frame with the logo image at the top and navigation bar immediately below. Footer containing contact and social network links.
            <p>Home Page:	Welcome message including image followed by most current post. </p>
            <p>News Page:	Articles and posts regarding newly created products and where to purchase them, event updates and other information.</p>
            <p>Gallery Page:	Sorted by category and sub-category. Images of designs past and present. Available pieces will be linked to purchase information.</p>
            <p>Shopping Page:	Links to online shopping venues and “how-to” for first time shoppers. </p>
            <p>Inspiration & Process Pages:	Articles about inspiration sources and the design process.</p>
            <p>About Page:	This page will give a history of Bead-n-Type and the evolution of the company. The designer and her support.</p>
            <p>Events Page:	Upcoming shows and markets where items can be purchased directly and the best selection is available.</p>
            <p>Contact Page:	Links to social media sites, email, website, and online shopping venues. </p>
            
            <p><b>Style Guide</b></p>
            
