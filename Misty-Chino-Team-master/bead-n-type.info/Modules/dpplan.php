<li>
    <h4>How</h4>
</li>
</ul>
<p>If I can spend one solid, uninterrupted hour a day completely focused. I will be able to show more progress than that same work done during an evening of distractions from family and devices. Extending that hour, when possible, to two hours would prove even more beneficial and productive. Using the momentum established during the initial hour to swing into the second hour.</p>
<p>The key to making the most of these hours is preparation. Reading the assignment, getting all my “ducks in a row” before I try to launch into a project. By preparing ahead of time, I can keep that focus on the task without leaving it to find out more information or gather other details that should have been gathered as part of the preparation.</p>
