<p>With everything that is dividing my schedule into tiny fragments of time, slicing out a designated time isn’t easily achieved. However, there are times during the week, when I can be left to study. The prime time is on Mondays, Wednesdays and Fridays, between noon and 3 p.m. when my children are still at school and my morning commitments have been met. Other times are captured here and there; Tuesday and Thursday evenings, Saturday afternoons. But to schedule a time for deliberate practice, Mondays, Wednesdays and Fridays are the best.</p>
